package assignment4;
public class BooleanQuestion {
	private boolean answer;
	private int mark;
	
	public BooleanQuestion(boolean answer, int mark) {
		this.answer = answer;
		this.mark = mark;
	}

	public boolean getAnswer() {
		return answer;
	}

	public void setAnswer(boolean answer) {
		this.answer = answer;
	}

	public int getMark() {
		return mark;
	}

	public void setMark(int mark) {
		this.mark = mark;
	}
	
	

}
